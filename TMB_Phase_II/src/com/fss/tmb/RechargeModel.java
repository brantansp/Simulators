package com.fss.tmb;

public class RechargeModel {

	private String finalDBmessage;
	private String CategoryList[];
	private String CategoryId[];
	private String CategoryName[];
	private String CategoryInputs[][];
	private String beneficiaryList[];
	public String getFinalDBmessage() {
		return finalDBmessage;
	}
	public void setFinalDBmessage(String finalDBmessage) {
		this.finalDBmessage = finalDBmessage;
	}
	public String getUniqueCategoryID(int index) {
		return CategoryId[index];
	}
	public String[] getCategoryList() {
		return CategoryList;
	}
	public void setCategoryList(String[] categoryList) {
		CategoryList = categoryList;
	}
	public String[] getCategoryId() {
		return CategoryId;
	}
	public void setCategoryId(String[] categoryId) {
		CategoryId = categoryId;
	}
	public String[] getCategoryName() {
		return CategoryName;
	}
	public void setCategoryName(String[] categoryName) {
		CategoryName = categoryName;
	}
	public String[][] getCategoryInputs() {
		return CategoryInputs;
	}
	public void setCategoryInputs(String[][] categoryInputs) {
		CategoryInputs = categoryInputs;
	}
	public String[] getBeneficiaryList() {
		return beneficiaryList;
	}
	public void setBeneficiaryList(String nickNames) {
		this.beneficiaryList =  nickNames.split(";");
	}
}